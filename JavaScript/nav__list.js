const nav__linkLogIn = document.querySelector('.nav__link--log-in')
const nav__linkSignIn = document.querySelector('.nav__link--sign-in')

nav__linkAbout.addEventListener('click', () => {
    navAdd.classList.add('close')
    navAdd.classList.remove('open')
    heroBtnLeft.classList.remove('disabled')
    heroBtnRight.classList.remove('disabled')
    body.classList.remove('scroll--off')
    body.classList.add('scroll')
    navOpen = false

    headerNav.classList.add('close')
    headerNav.classList.remove('open')
    headerNavOpen = true
})

